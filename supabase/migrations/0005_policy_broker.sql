-- Out-of-band policy broker: append-only intents/events/halts + mutable lease.
-- Additive only. Apply with the Supabase migration runner; never from the LLM.

create table if not exists public.broker_intents (
  id bigint generated by default as identity primary key,
  idempotency_key text not null unique,
  intent_type text not null check (intent_type in ('flip_lane', 'move_capital', 'rotate_secret')),
  payload jsonb not null,
  created_at timestamptz not null default now()
);

create table if not exists public.broker_intent_events (
  id bigint generated by default as identity primary key,
  intent_id bigint not null references public.broker_intents(id),
  status text not null check (status in ('PENDING', 'EXECUTED', 'REJECTED', 'IN_DOUBT', 'HARD_STOP')),
  reason text,
  created_at timestamptz not null default now()
);

create table if not exists public.broker_intent_attempts (
  id bigint generated by default as identity primary key,
  intent_id bigint not null unique references public.broker_intents(id),
  holder text not null check (length(trim(holder)) > 0),
  claimed_at timestamptz not null default now()
);

create table if not exists public.broker_halts (
  id bigint generated by default as identity primary key,
  scope text not null,
  action text not null check (action in ('halt', 'clear')),
  reason text,
  operator text,
  created_at timestamptz not null default now(),
  constraint broker_clear_requires_operator check (action <> 'clear' or length(trim(operator)) > 0)
);

create table if not exists public.broker_leases (
  name text primary key,
  holder text not null,
  expires_at timestamptz not null
);

